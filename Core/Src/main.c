  HAL_Delay(1000);

  // Initialize motor handle (UART7, PA3, ID 1)
  AX12_Init(&ax12, &huart7, GPIOA, GPIO_PIN_3, 0x01);

  // Enable Torque (The motor must be enabled to move)
  AX12_TorqueEnable(&ax12, 1);
